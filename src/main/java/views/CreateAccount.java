    public void getConfirmation(String code, String email, String nname, String password, String perm, String adminpassword) {

        boolean check = cad.checkMainAdminPassword(adminpassword);
        if(!check) return;
        
        String result = (String) JOptionPane.showInputDialog(
                this,
                email + " mailinize gelen doğrulama kodunu giriniz.",
                "Doğrulama",
                JOptionPane.PLAIN_MESSAGE,
                null,
                null,
                ""
        );
        if (!result.equals(code)) {
            if (result == null) {
                return;
            }
            Dialogs.errorBox("Geçersiz kod!", "Hata!");
            getConfirmation(code, email, nname, password, perm, adminpassword);
        } else {
            cad.createAccount(nname, password, perm, adminpassword);
        }
    }
