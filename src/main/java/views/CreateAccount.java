    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        String nname = nickname.getText(), password = String.valueOf(caccountpassword.getPassword()), repassword = String.valueOf(caccountrepassword.getPassword()), perm = permission.getItemAt(permission.getSelectedIndex()), adminpassword = String.valueOf(caccountmainadmin.getPassword());

        if (nname == null || nname.equals("")) {
            Dialogs.infoBox("Lütfen bir isim giriniz!", "Hata");
            return;
        }
        
        if (password.length() < 8) {
            Dialogs.infoBox("Şifreniz 8 karakter ya da daha uzun olmalı!", "Kısa Şifre");
            return;
        }

        if (!password.equals(repassword)) {
            Dialogs.infoBox("Şifreler uyuşmuyor!", "Uyumsuz Şifre");
            return;
        }
        
        if (adminpassword.length() < 1) {
            Dialogs.infoBox("Lütfen Ana Admin şifresini giriniz!", "Hata");
            return;
        }
        
        if (perm.equals("Ana Admin")) {
            int a = JOptionPane.showOptionDialog(this, "Oluşturmakta olduğunuz hesaba Ana Admin yetkisi vermek istediğinize emin misiniz?", "Uyarı", 0, 0, null, new String[]{"Evet", "Hayır"}, "Hayır");
            if (a == 1) {
                return;
            }
        }
        boolean check = cad.checkAccount(nname, password);
        if (check == false) {
            return;
        }
        int number = new Random().nextInt(999999);
        String code = String.format("%06d", number);
        String email = cad.getAddress();
        if (email == null) {
            Dialogs.errorBox("Veritabanına kayıtlı bir mail bulunamadı, bu sebeple otomatik kayıt yapılamıyor.\nLütfen veritabanına el ile yeni bir mail adresi kaydediniz.", "Hata!");
            return;
        }
        String result = mail.sendEmail(email, "Hesap Oluşturma - Doğrulama Kodu", "Doğrulama kodunuz:\n" + code);
        if (result.equals("connection error")) {
            Dialogs.errorBox("Mail bağlantısında bir sorun oluştu! Lütfen internet bağlantınızı kontrol ediniz.", "Bağlantı Hatası");
            return;
        } else if (result.equals("error")) {
            Dialogs.errorBox("Mail gönderilirken bir sorun oluştu! Lütfen daha sonra tekrar deneyiniz.", "Hata");
            return;
        }
        getConfirmation(code, email, nname, password, perm, adminpassword);
    }//GEN-LAST:event_createButtonActionPerformed

