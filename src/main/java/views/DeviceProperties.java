    private void showAssignedProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showAssignedProfileActionPerformed
        if(assignedProfileID.getText()==null||assignedProfileID.getText().equals("")) return;
        boolean check = dp.checkProfile(assignedProfileId);
        if(!check) return;
        ProfileProperties pp = new ProfileProperties(assignedProfileId);
        pp.setVisible(true);
    }//GEN-LAST:event_showAssignedProfileActionPerformed

    private void removeAssignmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeAssignmentButtonActionPerformed
        String deviceId = deviceID.getText(), devicename = deviceName.getText(), profileID = assignedProfileID.getText();
        if(profileID==null||profileID.equals("")) return;
        dp.removeAssignment(deviceId, devicename, profileID);
        assignedProfileID.setText("");
        assignedProfileName.setText("");
        if(status.getText().equals("Kullanımda"))
            status.setText("Boşta");
        chooseProfile.setEnabled(true);
        assignButton.setEnabled(true);
    }//GEN-LAST:event_removeAssignmentButtonActionPerformed

    private void statusUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusUpdateButtonActionPerformed
        String devicestatus = chooseStatus.getSelectedItem().toString(), ID = deviceID.getText(), id = assignedProfileID.getText();
        if((devicestatus.equals("Boşta") || devicestatus.equals("Kullanım Dışı") || devicestatus.equals("Tamirde")) && status.getText().equals("Kullanımda") && id!=null&&!id.equals("")){
            Dialogs.errorBox("Durumu değiştirmek için lütfen atamayı kaldırınız!", "Hata");
            return;
        }
        dp.updateStatus(ID, devicestatus);
        status.setText(devicestatus);
    }//GEN-LAST:event_statusUpdateButtonActionPerformed

    private void assignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignButtonActionPerformed
        String profileName = chooseProfile.getSelectedItem().toString(), deviceId = deviceID.getText(), devicename = deviceName.getText(), devicestatus = status.getText(), id = assignedProfileID.getText();
        if(!devicestatus.equals("Boşta") && (devicestatus.equals("Kullanımda") && (id!=null && !id.equals("")))){
            Dialogs.errorBox("Cihaz zaten bir profile atanmış durumda ya da atamaya uygun değil!", "Hata");
            return;
        }else if(devicestatus.equals("Kullanım Dışı") || devicestatus.equals("Tamirde")){
            Dialogs.errorBox("Cihaz atamaya uygun değil!", "Hata");
            return;
        }
        String profileID = dp.assign(profileName, deviceId, devicename);
        assignedProfileID.setText(profileID);
        assignedProfileName.setText(profileName);
        assignedProfileId = profileID;
        status.setText("Kullanımda");
    }//GEN-LAST:event_assignButtonActionPerformed

