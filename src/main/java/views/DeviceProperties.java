        Pair<Date, String[]> p = dp.getDevice(ID);
        Date devicewarranty = p.getKey();
        String[] details = p.getValue();

        String categoryname = details[0], devicename = details[1], devicestatus = details[2], deviceAssignedProfile = details[3], deviceAssignedProfileID = details[4], 
                deviceRemainingWarranty = details[5], serialnum = details[6];
        
        List<String[]> properties = dp.getDeviceProperties(devicename, categoryname);
        DefaultTableModel model=(DefaultTableModel) propertiesTable.getModel(); //tablo nesnemizi obje haline getiriyoruz.
        model.setRowCount(0); //veri listelenmeden önce başlangıç satırının sıfır yapıyor.
        for (String[] eachRow : properties) {
            model.addRow(eachRow); //obje haline gelen tablomuza metod yardımıyla verileri while döngüsü içerisinde satır satır ekliyoruz.
        }
        
        List<String[]> values = sdb.getCategoriesFull();
        DefaultComboBoxModel category = (DefaultComboBoxModel) deviceCategoryComboBox.getModel();
        for (String[] eachRow : values) {
            if(category.getIndexOf(eachRow[1]) == -1){
                category.addElement(eachRow[1]); //obje haline gelen comboboxa metod yardımıyla verileri while döngüsü içerisinde satır satır ekliyoruz.
            }
        }
        
        List<String[]> profiles = dp.getProfilesFull();
        DefaultComboBoxModel profile = (DefaultComboBoxModel) chooseProfile.getModel();
        for (String[] eachRow : profiles) {
            if(profile.getIndexOf(eachRow[1]) == -1){
                profile.addElement(eachRow[1]); //obje haline gelen comboboxa metod yardımıyla verileri while döngüsü içerisinde satır satır ekliyoruz.
            }
        }

        category.setSelectedItem(categoryname);
        deviceName.setText(devicename);
        deviceID.setText(ID);
        serialNum.setText(serialnum);
        status.setText(devicestatus);
        assignedProfileID.setText(deviceAssignedProfileID);
        assignedProfileName.setText(deviceAssignedProfile);
        warrantyDate.setDate(devicewarranty);
        remainingWarranty.setText(deviceRemainingWarranty);
        assignedProfileId = deviceAssignedProfileID;
