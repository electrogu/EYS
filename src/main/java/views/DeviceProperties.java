    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String newName = deviceName.getText(), newWarrantyDate = warrantyDate.getDate().toString(), newCategory = deviceCategoryComboBox.getSelectedItem().toString(), deviceId = deviceID.getText();
        if(newName.equals(currentName) && newWarrantyDate.equals(currentWarrantyDate) && newCategory.equals(currentCategory)){
            editButton.setEnabled(true); updateButton.setEnabled(false);
            deviceName.setEnabled(false); warrantyDate.setEnabled(false);
            deviceCategoryComboBox.setEnabled(false);
            return;
        }
        Date date = warrantyDate.getDate();
        LocalDate localdate = date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        dp.edit(deviceId, newName, localdate, newCategory);
        editButton.setEnabled(true); updateButton.setEnabled(false);
        deviceName.setEnabled(false); warrantyDate.setEnabled(false);
        deviceCategoryComboBox.setEnabled(false);
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int n = Dialogs.questionBox(this, "Bu cihazı silmek istediğinize emin misiniz?", "Uyarı");
        if(n!=0) return;
        String devicename = deviceName.getText();
        dp.deleteDevice(ID, devicename, assignedProfileId);
        removeAssignmentButton.setEnabled(false); chooseStatus.setEnabled(false); statusUpdateButton.setEnabled(false); chooseProfile.setEnabled(false);
        assignButton.setEnabled(false); showDeviceLog.setEnabled(false); editButton.setEnabled(false); updateButton.setEnabled(false);
        deleteButton.setEnabled(false); deviceName.setEnabled(false); warrantyDate.setEnabled(false); deviceCategoryComboBox.setEnabled(false);
    }//GEN-LAST:event_deleteButtonActionPerformed

