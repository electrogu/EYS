    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        String[] details = cpd.getCategory(ID);

        String categoryname = details[0], propertyNumber = details[1], dateofcreation = details[2], regdevicenumber = details[3];
        
        Pair<List<String>, String[]> p = cpd.getCategoryDetails(categoryname);
        List<String> regDevices = p.getKey();
        String[] propertynames = p.getValue();
        
        DefaultTableModel model=(DefaultTableModel) regDevicesTable.getModel(); //tablo nesnemizi obje haline getiriyoruz.
        model.setRowCount(0); //veri listelenmeden önce başlangıç satırının sıfır yapıyor.
        regDevices.forEach(eachRow -> {
            model.addRow(new String[] {eachRow}); //obje haline gelen tablomuza metod yardımıyla verileri while döngüsü içerisinde satır satır ekliyoruz.
        });
        DefaultTableModel model1=(DefaultTableModel) propertyNames.getModel(); //tablo nesnemizi obje haline getiriyoruz.
        model1.setRowCount(0); //veri listelenmeden önce başlangıç satırının sıfır yapıyor.
        for (String eachRow : propertynames) {
            model1.addRow(new String[] {eachRow}); //obje haline gelen tablomuza metod yardımıyla verileri while döngüsü içerisinde satır satır ekliyoruz.
        }
        categoryName.setText(categoryname);
        categoryID.setText(ID);
        propertyNum.setText(propertyNumber);
        regDeviceNum.setText(regdevicenumber);
        dateOfCreation.setText(dateofcreation);
    }//GEN-LAST:event_formComponentShown

