    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String newName = categoryName.getText();
        if(newName.equals(currentCategoryName)){
            editButton.setEnabled(true); updateButton.setEnabled(false);
            categoryName.setEnabled(false);
            return;
        }
        cpd.edit(ID, currentCategoryName, newName);
        editButton.setEnabled(true); updateButton.setEnabled(false);
        categoryName.setEnabled(false);
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCategoryActionPerformed
        String[] categoryChoices = cpd.getAllCategories(categoryName.getText());
        
        Pair<Integer, Object> p = Dialogs.comboBox(this, categoryChoices, "Yer değiştirilecek kategoriyi seçiniz.");
        if(p.getKey()!=0) return;
        if(p.getValue()==null) return;
        String newCategoryName = p.getValue().toString();
        int n = Dialogs.questionBox(this, "Bu kategoriyi silip bu kategoriye kayıtlı cihazları "+newCategoryName+" kategorisine kaydetmek istediğinize emin misiniz?", "Uyarı");
        if(n!=0) return;
        currentCategoryName = categoryName.getText();
        cpd.deleteAndReplaceCategory(ID, currentCategoryName, newCategoryName);
    }//GEN-LAST:event_deleteCategoryActionPerformed

